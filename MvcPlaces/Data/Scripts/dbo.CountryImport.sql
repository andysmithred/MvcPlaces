CREATE TABLE [dbo].[Country] (
    [Id]               INT            IDENTITY (1, 1) NOT NULL,
    [Name]             NVARCHAR (50)  NOT NULL,
    [FullName]         NVARCHAR (MAX) NULL,
    [ContinentId]      INT            NOT NULL,
    [Population]       INT            NULL,
    [Area]             INT            NULL,
    [LandBoundaries]   INT            NULL,
    [Coastline]        INT            NULL,
    [HighestPoint]     INT            NULL,
    [LowestPoint]      INT            NULL,
    [HighestPointName] NVARCHAR (MAX) NULL,
    [LowestPointName]  NVARCHAR (MAX) NULL,
    [IrrigatedLand]    INT            NULL,
    [BirthRate]        FLOAT (53)     NULL,
    [DeathRate]        FLOAT (53)     NULL,
    [LifeExpectancy]   FLOAT (53)     NULL,
    [Government]       NVARCHAR (50)  NULL,
    [GDP]              FLOAT (53)     NULL,
    [GDPPerCapita]     FLOAT (53)     NULL,
    [LabourForce]      FLOAT (53)     NULL,
    [Airports]         INT            NULL,
    [Railways]         INT            NULL,
    [Roads]            INT            NULL,
    [ISOCode]          NCHAR (2)      NULL,
    [Latitude]         FLOAT (53)     DEFAULT ((0)) NOT NULL,
    [Longitude]        FLOAT (53)     DEFAULT ((0)) NOT NULL,
    [Zoom]             INT            DEFAULT ((10)) NOT NULL,
    [ImportId] INT NULL, 
    PRIMARY KEY CLUSTERED ([Id] ASC),
    CONSTRAINT [FK_Country_To_Continent] FOREIGN KEY ([ContinentId]) REFERENCES [dbo].[Continent] ([Id])
);

